package com.hambre.model;

public class ComboDTO {

	private int comId;
	
	private String comNom;
	private int comPrc;
	private String tipPrdNom;
	private String medNom;
	public int getComId() {
		return comId;
	}
	public void setComId(int comId) {
		this.comId = comId;
	}
	public String getComNom() {
		return comNom;
	}
	public void setComNom(String comNom) {
		this.comNom = comNom;
	}
	public int getComPrc() {
		return comPrc;
	}
	public void setComPrc(int comPrc) {
		this.comPrc = comPrc;
	}
	public String getTipPrdNom() {
		return tipPrdNom;
	}
	public void setTipPrdNom(String tipPrdNom) {
		this.tipPrdNom = tipPrdNom;
	}
	public String getMedNom() {
		return medNom;
	}
	public void setMedNom(String medNom) {
		this.medNom = medNom;
	}
	
	
}
